public class NumberWork {
    
    public int addNumbers(int n1, int n2){
        return (n1+n2);
    }
    
    public int subtractNumbers(int n1, int n2){
        return (n1-n2);
    }
   
    public int multiplyNumbers(int n1, int n2){
        return (n1*n2);
    }
    
    public int divideNumbers(int n1, int n2){
        return (n1/n2);
    }
    
    
    
}
